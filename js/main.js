let container = document.getElementById("container");

function PretrazivanjeAkcija(pretrazenoMjesto, e) {
  e.preventDefault();
    
  container.innerHTML = ""; //STAVLJA HTML KOD UNUTAR DIVA CONTAINER NA PRAZNO KAKO BI U NJEGA MOGLI DODAVATI SAMO KARTICE KOJE SU NAM POTREBNE

  //AKO JE KORISNIK PRAZNO PRETRAZIO ONDA DODAVAMO SVE KARTICE ALI AKO JE UNIO NESTO ONDA FILTRIRAMO AKCIJE PREMA TOME
  if (pretrazenoMjesto == "") {
    for (let i = 0; i < akcije.length; i++) { DodavanjeKartica(akcije[i]); }
  }
  else {
    for (let i = 0; i < akcije.length; i++) {
      let dodano = false
      let trenutnaMjesta = akcije[i].mjesto.split(', '); //STAVALJAMO TRENUTNU akcije KOJA STOJI U NIZU akcije I IMA INDEX i
          
      for(let j = 0; j < trenutnaMjesta.length; j++){
        //PREGLEDAVAMO JE LI MJESTO KOJE SMO PRETRAZILI JEDANKO TRENUTNOJ AKCIJI I AKO JE DODAVAMO KARTICU SA INFORMACIJAMA
        if ((trenutnaMjesta[j].includes(pretrazenoMjesto) || trenutnaMjesta[j].toLowerCase().includes(pretrazenoMjesto)) && !dodano) { DodavanjeKartica(akcije[i]); dodano = true; }
      }
    }
  }
}

function DodavanjeKartica(trenutnaAkcija){
    container.innerHTML += `<div class="card akcija-card"> 
       <img src="../assets/img/${trenutnaAkcija.slika}" class="card-img-top" alt="${trenutnaAkcija.alt}">
        <div class="card-body">
            <h5 class="card-title">${trenutnaAkcija.ime}, ${trenutnaAkcija.mjesto}</h5>
            <p class="card-text">${trenutnaAkcija.opis}</p>
            <a href="${trenutnaAkcija.link}" class="btn btn-success akcije-card-btn">Posjetite stranicu akcije</a>
        </div>
    </div>`;  
}



